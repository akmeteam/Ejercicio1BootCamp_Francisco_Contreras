package ejercicios;

public class NumeroParOImpar {
    public static void main(String[] args) {
        int a = 15;
        //Muestra si es o no par o impar.
        if (a % 2 == 0) {
            System.out.println(a + " es un número par.");
        } else {
            System.out.println(a + " es un número impar.");
        }
    }
}
